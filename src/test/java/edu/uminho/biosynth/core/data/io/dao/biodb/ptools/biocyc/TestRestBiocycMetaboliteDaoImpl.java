package edu.uminho.biosynth.core.data.io.dao.biodb.ptools.biocyc;

import static org.junit.Assert.*;

import org.junit.Test;

import edu.uminho.biosynth.core.components.biodb.biocyc.BioCycMetaboliteEntity;

public class TestRestBiocycMetaboliteDaoImpl {

	@Test
	public void testWATER() {
		RestBiocycMetaboliteDaoImpl dao = new RestBiocycMetaboliteDaoImpl();
		dao.setLocalStorage("D:/home/data/biocyc/");
		dao.setSaveLocalStorage(true);
		dao.setUseLocalStorage(true);
		
		BioCycMetaboliteEntity cpd = dao.getMetaboliteById("WATER");

		assertEquals("WATER", cpd.getEntry());
		assertEquals("H2O1", cpd.getFormula());
		assertEquals("1S/H2O/h1H2", cpd.getInChI());
	}

	@Test
	public void testCPD_10773() {
		RestBiocycMetaboliteDaoImpl dao = new RestBiocycMetaboliteDaoImpl();
		dao.setLocalStorage("D:/home/data/biocyc/");
		dao.setSaveLocalStorage(true);
		dao.setUseLocalStorage(true);
		
		BioCycMetaboliteEntity cpd = dao.getMetaboliteById("CPD-10773");
		
		assertEquals("CPD-10773", cpd.getEntry());
		assertEquals("C5H8O4", cpd.getFormula());
		assertEquals("1S/C5H8O4/c1-5(8)4(7)3(6)2-9-5/h3,6,8H,2H2,1H3/t3-,5+/m0/s1", cpd.getInChI());
	}
}
