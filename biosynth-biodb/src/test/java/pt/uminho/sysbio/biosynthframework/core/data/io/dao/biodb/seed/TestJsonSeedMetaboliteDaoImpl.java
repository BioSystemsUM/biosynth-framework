package pt.uminho.sysbio.biosynthframework.core.data.io.dao.biodb.seed;

import static org.junit.Assert.*;

import org.junit.After;
import org.junit.AfterClass;
import org.junit.Before;
import org.junit.BeforeClass;
import org.junit.Test;

import pt.uminho.sysbio.biosynthframework.biodb.seed.SeedMetaboliteEntity;

public class TestJsonSeedMetaboliteDaoImpl {

	private static JsonSeedMetaboliteDaoImpl metaboliteDao;
	private static String FOLDER = "D:/home/data/seed";
	
	@BeforeClass
	public static void setUpBeforeClass() throws Exception {
		metaboliteDao = new JsonSeedMetaboliteDaoImpl(FOLDER);
	}

	@AfterClass
	public static void tearDownAfterClass() throws Exception {
	}

	@Before
	public void setUp() throws Exception {
	}

	@After
	public void tearDown() throws Exception {
	}

	@Test
	public void test_get_cpd00001_success() {
		SeedMetaboliteEntity cpd = metaboliteDao.getMetaboliteByEntry("cpd00001");
		
		assertEquals("cpd00001", cpd.getEntry());
		assertEquals(0, cpd.getDefaultCharge());
		assertEquals(1, cpd.getStructures().size());
		assertEquals(2, cpd.getPks().size());
		assertEquals(22, cpd.getCrossreferences().size());
	}
	
	@Test
	public void test_get_cpd00021_success() {
		SeedMetaboliteEntity cpd = metaboliteDao.getMetaboliteByEntry("cpd00201");
		
		assertEquals("cpd00201", cpd.getEntry());
		assertEquals(-2, cpd.getDefaultCharge());
		assertEquals(1, cpd.getStructures().size());
		assertEquals(10, cpd.getPks().size());
		assertEquals(20, cpd.getCrossreferences().size());
	}
	
	@Test(expected=RuntimeException.class)
	public void test_get_by_id_exception() {
		metaboliteDao.getMetaboliteById(3);
	}

}
